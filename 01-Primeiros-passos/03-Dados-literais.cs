//  Literal char: um caractere alfanumérico com aspas simples
Console.WriteLine('b');

//  Literal int: valor numérico inteiro (sem frações)
Console.WriteLine(123);

//   Literal decimal: um número que inclui valores após o ponto decimal. Para criar um literal decimal, acrescente a letra m após o número
Console.WriteLine(12.3m);

//  Literal bool: um valor representando true ou false
Console.WriteLine(true);
Console.WriteLine(false);